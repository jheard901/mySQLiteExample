- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    return [self.contactsArray count];
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"ContactCell" forIndexPath:indexPath];
    
    //maybe can opt of this as explained in this example: http://www.appcoda.com/sqlite-database-ios-app-tutorial/
    //NSInteger indexOfName;
    //NSInteger indexOfAddress; //not using this for this example
    //NSInteger indexOfPhone;
    
    //skip the first index, because that stores the id, the values after that is what we want
    cell.textLabel.text = [NSString stringWithFormat:@"%@", [[self.contactsArray objectAtIndex:indexPath.row] objectAtIndex:1]];
    cell.detailTextLabel.text = [NSString stringWithFormat:@"%@", [[self.contactsArray objectAtIndex:indexPath.row] objectAtIndex:3]];
    
    return cell;
}


